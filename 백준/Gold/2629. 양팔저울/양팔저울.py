import sys
from itertools import combinations
input = sys.stdin.readline

def weighing(weights, targets):
    cases = [0]
    visited = [0] * (sum(weights) + 1)
    result = {i : 'N' for i in targets}

    for w in weights:
        
        for i in range(len(cases)):
            tmp = [cases[i] + w]
            tmp.append(abs(w - cases[i]))
        
            for t in tmp:
                if visited[t] == 0:
                    visited[t] = 1
                    cases.append(t)
                else:
                    continue

                if t in targets:
                    result[t] = 'Y'
    
    return result
            

